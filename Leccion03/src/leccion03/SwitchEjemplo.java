public class SwitchEjemplo {
    
    public static void main(String args []){
        
        int mes = 8; 
        
           //Switch
        switch(mes){
            case 1:
                System.out.println("Enero");
                break;
            case 2:
                System.out.println("Febrero");
                break;
            case 3:
                System.out.println("Marzo");
                break;
            case 4:
                System.out.println("Abril");
                break;
            default:
                System.out.println("El mes no existe.");
                break;
        
        }
        
     
        
        
        
    }
}
